package com.groupdocs.viewer.samples.spring.model.response;

/**
 * The type Rotate page response.
 */
public class RotatePageResponse {
    private int resultAngle;
    private boolean success;

    /**
     * Gets result angle.
     * @return the result angle
     */
    public int getResultAngle() {
        return resultAngle;
    }

    /**
     * Sets result angle.
     * @param resultAngle the result angle
     */
    public void setResultAngle(int resultAngle) {
        this.resultAngle = resultAngle;
    }

    /**
     * Is success boolean.
     * @return the boolean
     */
    public boolean isSuccess() {
        return success;
    }

    /**
     * Sets success.
     * @param success the success
     */
    public void setSuccess(boolean success) {
        this.success = success;
    }

}
